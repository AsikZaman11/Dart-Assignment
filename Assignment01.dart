void main() {
  const int a = 6;
  const int b = 3;

  //addition
  var sum = a + b;
  print("Addition: $a + $b = $sum");

  //subtraction
  var sub = a - b;
  print("Subtraction: $a - $b = $sub");

  //multiplication
  var mul = a * b;
  print("Multiplication: $b * $a =  $mul");

  //division
  var div = a / b;
  print("Division: $b / $a =  $div");

  //modulus
  var mod = a % b;
  print("Modulus: $b % $a =  $mod");
}
